// REMAINDER OPERATOR %
let newX = 7;
console.log("The % operator in the next statement tells us that nextX equals the remainder of 7 divided by 3")
let nextX = newX % 3;
console.log("nextX = newX % 3")
console.log("The value of nextX will be 1 because 3 fits two times inside 7, and 1 is the remainder")
console.log(nextX);